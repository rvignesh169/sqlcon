package retrivedatafromtable;

public class Employee {
	private int eid;
	private String ename ;
	private long esal;
	private int ejyear;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public long getEsal() {
		return esal;
	}
	public void setEsal(long esal) {
		this.esal = esal;
	}
	public int getEjyear() {
		return ejyear;
	}
	public void setEjyear(int ejyear) {
		this.ejyear = ejyear;
	}
	
	
}
